                                    <p><strong>Introduction </strong></p>
                                    <p>AACC will this year commemorate June 16, the Day of the African Child. The
                                        Day of the African Child has been celebrated on June 16 every year since
                                        1991, when it was first initiated by the Organisation of African Unity (OAU)
                                        now known as the African Union (AU). It honors the children and the young
                                        people who participated in the Soweto Uprising in 1976 on that day. It also
                                        raises awareness of the continuing need for improvement of the education
                                        provided to African children.</p>
                                    <p>In Soweto, South Africa, on June 16, 1976, about ten thousand black school
                                        children marched in a column more than half a mile long, protesting the poor
                                        quality of their education and demanding their right to be taught in their
                                        own language. This year’s theme is “<strong>Eliminating Harmful Practices
                                            Affecting Children: Progress on Policy & Practice since 2013</strong>”.
                                    </p>
                                    <p>In line with the history of 16 June, 1976 and the current challenges
                                        affecting young people in the continent AACC will celebrate this day by
                                        convening a public webinar to raise awareness on the day and Africa: My
                                        Home. My Future. campaign. The webinar question will be: <em>Decolonizing
                                            the education system and changing the mindset in Africa. What is the
                                            role of the youth? </em></p>
                                    <p><strong>Background</strong></p>
                                    <p>The African continent is a host to a large population of young people.
                                        Estimates say that by the year 2050 the current youth population will be
                                        double<a><sup><sup>[1]</sup></sup></a>.
                                        Undoubtedly, young people are the leaders of today, who need to take up the
                                        challenge to transform Africa. As Africa’s youthful population continues to
                                        rise, there is uncertainty over the continent’s preparedness to tap this
                                        resource for economic growth. Yet the continent has challenges that are
                                        politically, economically, and socially affecting young people.&nbsp;</p>
                                    <p>There is widespread frustration and agitation by and among young people over
                                        limited economic opportunities, growing corruption, rising unemployment, and
                                        limited opportunities for political participation<a><sup><sup>[2]</sup></sup></a>.
                                        This growing frustration
                                        is increasingly manifested in migration of young people from the continent.
                                        Many young people are willing to risk drowning in the Mediterranean Sea in
                                        the quest to get to Europe and other developed countries rather than stay in
                                        the continent. The motivation for migrating is mostly to pursue economic
                                        opportunities and an environment to succeed. The reality is that most of the
                                        young people fail to succeed and end up living in appalling conditions.</p>
                                    <p>The huge youth demography that should offer tremendous dividends to the
                                        continent has seemingly been seen as a mirage. Bulk of the population of the
                                        continent are victims of the plethora of vices such as irregular migration
                                        and human trafficking, violent extremism, xenophobia, drug and substance
                                        addictions. These vices and many others have continued to impact on the
                                        wellbeing of the continent and its people. It is a disturbing situation.
                                        AACC is disturbed and is optimistic that the narrative can change. Africa is
                                        immeasurably endowed with all kinds of resources including impressive human
                                        capital resources, some of whom are excellently providing global leadership
                                        in corporate and multilateral institutions, but Africa stills lags behind
                                        with leveraging on these gifts and potentials for the common good of all
                                        people. Something fundamental is missing. The search for the missing thing
                                        and link will continue until Africa is changed for the better, and this can
                                        only be achieved if the youth of Africa think and act differently in a
                                        timely manner.</p>
                                    <p><strong>The rationale of the webinar</strong></p>
                                    <p>Africa: My Home. My Future. is a continental campaign that seeks to mobilize
                                        a critical mass of youth from across the continent with an intentional focus
                                        on one of AACC’s programmatic thematic areas; “Youth and African
                                        Patriotism”. The campaign is a movement that aims to inspire young people to
                                        be and remain patriotic to their countries and Africa at large by utilizing
                                        their talents towards building a thriving continent.</p>
                                    <p>The campaign led by the All Africa Conference of Churches (AACC) will
                                        continue to create and sustain in profound ways a generational movement made
                                        up of youth living in the continent and in the diaspora. It is expected that
                                        the youth that will be mobilized within the constituency of the AACC are
                                        youth that have demonstrated some level of being patriotic and passionate
                                        about Africa. The movement will stimulate conversations amongst young people
                                        of Africa, on various issues affecting them and impacting the integrity of
                                        the continent.</p>
                                    <p>In recognition of the crucial role that youth can play as agents of change,
                                        the AACC deliberately launched the campaign that will culminate in the
                                        convocation of youth biennially in different countries of choice where
                                        thousands of youth will congregate to reflect, harvest, energize, challenge,
                                        evolve and chart the agenda for Africa’s transformation while keeping their
                                        eyes on national, continental and global development agendas.</p>
                                    <p>AACC is organizing a series of webinars to promote the campaign Africa: My
                                        Home. My Future. in the run up to the All Africa Youth Congress scheduled
                                        for Ghana this year from 31 October to 04 November 2022.</p>
                                    <p><strong>Main objective</strong></p>
                                    <p>Youth are inspired to contribute to the transformation of Africa.</p>
                                    <p><strong>Specific Objectives of the webinar</strong></p>
                                    <ol>
                                        <li>To celebrate Day of the African Child</li>
                                        <li>To increase awareness on the campaign slogan Africa: My Home. My Future.
                                        </li>
                                        <li>To create a viable space for connectivity and networking</li>
                                        <li>To accelerate constructive conversations and innovative actions that
                                            display African potentialities and abundance.</li>
                                    </ol>
                                    <p><strong>Expected outcomes</strong></p>
                                    <ol>
                                        <li>Increased awareness of the importance of the Day of the African Child
                                        </li>
                                        <li>Increased awareness of the campaign <strong>Africa: My Home. My
                                                Future.</strong></li>
                                        <li>Created potent space for youth engagements and constructive dialogue on
                                            the future of Africa</li>
                                    </ol>
                                    <p><strong>Guiding Question for the webinar</strong></p>
                                    <p><em>Decolonizing the education system and changing the mindset in Africa.
                                            What is the role of the youth? </em></p>
                                    <p><strong>Methodology</strong></p>
                                    <p>This will be held in a conversation format on zoom, where the guest speaker
                                        will respond to the webinar question in a one-on-one session. The speaker
                                        will have an opportunity to freely present in the first 30 minutes. After
                                        that, the moderator and participants will have an opportunity to converse
                                        with the speaker.</p>
                                    <p>The webinar will feature a Pan African speaker.</p>
                                    <p><strong>Target beneficiaries (Participants)</strong></p>
                                    <p>100 youth leaders from the constituency of AACC in forty-three (43) African
                                        countries will be direct beneficiaries of the knowledge-based webinar that
                                        will spur them to ask critical questions and act reasonably for the greater
                                        good of their countries and the continent. Thousands of other youth will be
                                        indirect beneficiaries as the direct beneficiaries are expected to conduct
                                        stepdown engagements after the webinar.</p>
