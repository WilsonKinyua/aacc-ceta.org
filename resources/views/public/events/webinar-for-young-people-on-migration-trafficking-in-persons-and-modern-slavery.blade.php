                                        migration to Africa also experienced tremendous growth in recent years,
                                        increasing from just below 2.5 million in 1990 to more than 5 million in
                                        2017. Africa in general and West Africa, in particular, have become a hub
                                        for irregular migration and human trafficking; and this is a major problem
                                        for the continent and its stakeholders. The reasons for such phenomena are
                                        many and varied. Among others, and to name but a few, we could point to the
                                        fact that the region is experiencing political instability with deep
                                        economic turbulences; extreme poverty causing a widespread desire among poor
                                        populations to migrate in search of better living conditions in the region
                                        or further afield; as well as traditional cultural practices such as widow
                                        inheritance causing women to flee from their villages hence falling into the
                                        hands of, in particular, even paying to be smuggled across borders.
                                        Furthermore, it has been noticed that West Africa has - compared to other
                                        regions - rather accessible/ organised migration routes for migrants
                                        destined to Europe, with flourishing networks and business for traffickers
                                        and smugglers. In the face of such a deplorable situation, corruption and
                                        lack of systematic cooperation and coordination among nations in the region
                                        has made it difficult and almost impossible for the various countries in the
                                        region to dismantle the phenomenon.</p>
