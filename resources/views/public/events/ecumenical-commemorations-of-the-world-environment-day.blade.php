                                        environmental public outreach and is celebrated differently by stakeholders
                                        across the world. The AACC will utilize the World Environment Day
                                        commemorations to amplify the voice of the Church on Climate Change and
                                        other related matters. As part of its preparations for participation at the
                                        COP 27, the AACC will run a week-long campaign on all its platforms
                                        including social media and available physical spaces. While environment is
                                        broad, the AACC will focus mainly on Climate Change in its messaging during
                                        this period as direct contribution to its five-year strategic plan that
                                        partly aims at addressing the impact of this crisis.</p>
