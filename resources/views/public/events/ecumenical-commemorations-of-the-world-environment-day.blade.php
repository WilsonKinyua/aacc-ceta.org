                                        <li><strong>The Problem</strong></li>
                                    </ol>
                                    <p>The United Nations Environment Programme (UNEP) (2021) notes that the world
                                        is in a climate emergency, “a code red for humanity” according to the UN
                                        Secretary-General<a >[1]</a>. The UNEP further
                                        indicates that the concentration of greenhouse gas (GHG) emissions in the
                                        atmosphere is wreaking havoc across the world and threatening lives,
                                        economies, health and food. The world is far from securing a global
                                        temperature rise to below 2°C as promised in the Paris Agreement. As an
                                        emerging major player in Climate Change issues, the Church is challenged to
                                        contribute in its own unique way to the efforts of addressing the impact of
                                        Climate Change.</p>
