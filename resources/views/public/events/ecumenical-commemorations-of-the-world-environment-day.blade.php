                                    <p>The environmental issues confronting the world today require immediate
                                        attention and effective action by all stakeholders. The rapidly changing
                                        environmental systems and depleting resources are causing substantial
                                        concern to government and business leaders around the world, yet, the Church
                                        should also be a major actor in finding sustainable solutions. As part of
                                        its ongoing work on environmental justice, the AACC utilizes all the
                                        available opportunities to raise awareness on key environmental issues as
